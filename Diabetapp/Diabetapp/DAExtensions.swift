import UIKit

extension UIColor {
    class func diabetRed() -> UIColor {
        return UIColor(red: 244/255, green: 76/255, blue: 91/255, alpha: 1)
    }
    
    class func diabetBlue() -> UIColor {
        return UIColor(red: 102/255, green: 161/255, blue: 231/255, alpha: 1)
    }
}